public class mothe {
	
	
	
	public int x,y;
	public int jia (){
		return this.x+this.y;
		
	}
	public int jian (){
		return this.x-this.y;
		
	}
	public int cheng (){
		return this.x*this.y;
		
	}
	public int chu (){
		return this.x/this.y;
		
	}

	public int jia(int x, int y) {

		return x + y;
	}

	public int cheng(int x, int y) {

		return x * y;
	}

	public int chu(int x, int y) {

		return x / y;

	}

	public int jian(int x, int y) {

		return x - y;

	}
}
